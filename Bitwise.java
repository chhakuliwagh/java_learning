// Bitwise operator
// complement(~):=~(n)=-(n+1)

class Bitwise
{
public static void main(String args[])
{
 int a=10,b=9;

 System.out.println("Bitwise And a&b= "+(a&b));

 System.out.println("Bitwise Or a|b= "+(a|b));

 System.out.println("Bitwise XOR a^b= "+(a^b));

 System.out.println("Bitwise Complement a~b= "+(~a));

}
}