// Shift Operator:
// Right Shift(>>)
// Left shift(<<)

class Shift
{
public static void main(String args[])
{
 int a=10;

 System.out.println("Right Shift by 1 of 10: "+(a>>1));

 System.out.println("Right Shift by 2 of 10: "+(a>>2));

 System.out.println("Left Shift by 1 of 10: "+(a<<1));

 System.out.println("Left Shift by 2 of 10: "+(a<<2));


}
}