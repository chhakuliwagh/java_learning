// Relational Operator

class Relational
{
   public static void main(String args[])
{
   int a=50,b=80;
   
   System.out.println("a<b : "+(a<b));
   System.out.println("a>b : "+(a>b));
   System.out.println("a<=b : "+(a<=b));
   System.out.println("a>=b : "+(a>=b));
   System.out.println("a!=b : "+(a!=b));
   System.out.println("a==b : "+(a==b));
  


}
}